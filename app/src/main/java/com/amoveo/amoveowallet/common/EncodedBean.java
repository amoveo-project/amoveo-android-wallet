package com.amoveo.amoveowallet.common;

public class EncodedBean {
    private String content;

    public EncodedBean() {
    }

    public EncodedBean(String content) {
        this.content = content;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}